CREATE TABLE EMPLOYEES(
ID NUMBER PRIMARY KEY ,
MANAGER_ID NUMBER NOT NULL CONSTRAINT MANAGER REFERENCES EMPLOYEES(id),
DATE_OF_JOINING DATE NOT NULL,
FULL_NAME VARCHAR(255) NOT NULL
);

/* 1-*Write a SQL query to fetch project code and count of employees (code, counter) sorted by counter in ascending order. example 48205501099, 4*/
SELECT P.CODE,COUNT(E.ID) FROM PROJECTS P INNER JOIN   EMPLOYEES E ON P.MANAGER_ID=E.ID

/* 2 -Write a query to fetch employee names of those who are not assigned to any project*/
SELECT E.FULL_NAME FROM PROJECTS P LEFT OUTER JOIN   EMPLOYEES E ON P.MANAGER_ID=E.ID


