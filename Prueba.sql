CREATE TABLE SALARIES(
ID NUMBER PRIMARY KEY NOT NULL,
EMPLOYEE_ID NUMBER NOT NULL CONSTRAINT SALARY_EMPLOYEE  REFERENCES EMPLOYEES(id),
PROJECT_ID NUMBER NOT NULL CONSTRAINT SALARY_PROJECT  REFERENCES PROJECTS(id),
SALARY NUMBER(10,2) 
);


CREATE TABLE PROJECTS(
ID NUMBER PRIMARY KEY NOT NULL,
CODE VARCHAR2(255) NOT NULL,
MANAGER_ID NUMBER NOT NULL CONSTRAINT MANAGER_PROJECT  REFERENCES EMPLOYEES(id),
BUDGET NUMBER(10,2)NOT NULL,
DATE_OF_BIGINNING DATE ,
DATE_OF_ENDING DATE
);

